/**
 * Full GET /api/party-packages payload for MSW + future backend parity.
 * Packages align with deployed Events page (6 cards). Catalog sections are
 * derived from events constants so add-ons / takeout / we-bring match the legacy UI.
 */

import type {
  RawEventsPageCatalogResponse,
  RawPartyPackageResponse,
} from '@/data/adapters/bookingAdapter'
import {
  ADDON_CATEGORY_CARDS,
  ADDON_GROUPS,
  TAKEOUT_CATEGORY_CARDS,
  TAKEOUT_GROUPS,
  TAKEOUT_IMAGE,
  WE_BRING_IMAGES,
  WE_BRING_SERVICES,
} from '@/components/user/events/constants'

export function buildRawEventsPageCatalog(): RawEventsPageCatalogResponse {
  return {
    addon_category_cards: ADDON_CATEGORY_CARDS.map(c => ({
      name: c.name,
      desc: c.desc,
      accent: c.accent,
      anchor: c.anchor,
    })),
    addon_groups: ADDON_GROUPS.map(g => ({
      title: g.title,
      anchor: g.anchor,
      items: g.items.map(i => ({ name: i.name, desc: i.desc, price: i.price })),
      border: g.border,
      bg: g.bg,
      price_bg: g.priceBg,
      price_color: g.priceColor,
    })),
    takeout_category_cards: TAKEOUT_CATEGORY_CARDS.map(c => ({
      name: c.name,
      desc: c.desc,
      accent: c.accent,
      anchor: c.anchor,
    })),
    takeout_groups: TAKEOUT_GROUPS.map(g => ({
      title: g.title,
      anchor: g.anchor,
      items: g.items.map(i => ({ name: i.name, desc: i.desc, price: i.price })),
      border: g.border,
      bg: g.bg,
      price_bg: g.priceBg,
      price_color: g.priceColor,
    })),
    we_bring_services: WE_BRING_SERVICES.map(s => ({ name: s.name, desc: s.desc })),
    we_bring_images: { ...WE_BRING_IMAGES },
    takeout_banner_image_url: TAKEOUT_IMAGE,
  }
}

